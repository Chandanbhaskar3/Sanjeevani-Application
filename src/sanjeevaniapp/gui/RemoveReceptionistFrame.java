    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String RecId = jcReceptionistId.getSelectedItem().toString();
        try{
            boolean result = ReceptionistDao.deleteReceptionistById(RecId) ;
            if(result){
                JOptionPane.showMessageDialog(null,"Receptionist removed successfully !!");
                jcReceptionistId.removeItem(RecId);
                if(jcReceptionistId.getItemCount() == 0){
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null,"All receptionist removed!!!");
                }
            }
            else
            JOptionPane.showMessageDialog(null, "Deletion unsuccessfull!!");
        }
        catch(SQLException ed){
            JOptionPane.showMessageDialog(null,"Error in DB in removeReceptionistFrame");
            ed.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

