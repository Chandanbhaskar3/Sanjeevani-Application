    private void loadRecId() {
        try{
            List <String> recId = ReceptionistDao.getAllRecId();
            for(String id : recId){
                jcReceptionistId.addItem(id);
            }
            if(jcReceptionistId.getItemCount() != 0)
               btnDelete.setEnabled(true);
        }
        catch(SQLException es){
            JOptionPane.showMessageDialog(null,"Error in DB removeReceptionistFrame");
            es.printStackTrace();
        }
        
    }
