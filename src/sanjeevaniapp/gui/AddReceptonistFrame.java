    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (validateInputs() == false) {
            JOptionPane.showMessageDialog(null, "Please fill all boxes");
            return;
        }
        String result = matchPassword();
        if (result != null) {
            JOptionPane.showMessageDialog(null, result);
            return;
        }
        try{
        UserPojo user = new UserPojo();
        user.setLoginId(loginId);
        String encPassword = new String(PasswordEncryption.getEncryptedPassword(password));
        user.setPassword(encPassword);
        user.setUserType("RECEPTIONIST");
        user.setUserName(selEmpName);
        ReceptionistPojo recep = new ReceptionistPojo();
        recep.setReceId(txtRecId.getText());
        recep.setGender(jComboBox1.getSelectedItem().toString());
        recep.setReceName(selEmpName);
        boolean resp1 = UserDao.addUser(user);
        boolean resp2 = ReceptionistDao.addReceptionist(recep);
        if(resp1 == true && resp2 == true){
            JOptionPane.showMessageDialog(null,"Receptionist added successfully ");
            clearAll();
            getNewReceptionistId();
            return;
        }
        JOptionPane.showMessageDialog(null,"Receptionist registration unsuccessfull");
        }
        catch(SQLException es){
            JOptionPane.showMessageDialog(null,"ERROR in DB addreceptionist");
            es.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

