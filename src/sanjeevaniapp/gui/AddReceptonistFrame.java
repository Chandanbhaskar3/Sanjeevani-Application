    private void getNewReceptionistId() {
        try {
            String id = ReceptionistDao.getNewRecId();
            txtRecId.setText(id);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB addReceptionistFrame");
            ex.printStackTrace();
        }
    }

    private void loadReceptionistDetails() {
        try {
            unregRecList = EmpDao.getUnRegisteredReceptionist();
            Set<String> recIdSet = unregRecList.keySet();
            for (String id : recIdSet) {
                jcEmpId.addItem(id);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB addreceptionist");
            ex.printStackTrace();
        }
    }

    private boolean validateInputs() {
        loginId = txtLoginId.getText().trim();
        char[] p1 = ps1.getPassword();
        char[] p2 = reps1.getPassword();
        if (loginId.isEmpty() || p1.length == 0 || p2.length == 0) {
            return false;
        }
        password = new String(p1).trim();
        rePassword = new String(p2).trim();
        return true;
    }

    private String matchPassword() {
        if (password.length() < 3 || rePassword.length() < 3) {
            return "Password must be greater than 3 characters";
        }
        if (password.equals(rePassword) == false) {
            return "Password doesn't match";
        }
        return null;
    }

    private void clearAll() {
        txtLoginId.setText("");
        ps1.setText("");
        reps1.setText("");
        unregRecList.remove(selEmpId);
        jcEmpId.removeItem(selEmpId);
        if (unregRecList.size() == 0) {
            JOptionPane.showMessageDialog(null, "All receptionist registered");
            ManageReceptionistFrame manageRecep = new ManageReceptionistFrame();
            manageRecep.setVisible(true);
            this.dispose();
        }
    }

