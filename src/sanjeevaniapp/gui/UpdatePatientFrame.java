    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        boolean isValid = validateInputs();
        if(!isValid){
            JOptionPane.showMessageDialog(null,"Please input all values");
            return;
         }
        try{
            PatientPojo pat = new PatientPojo();
            pat.setFirstName(fName);
            pat.setLastName(lName);
            pat.setCity(city);
            pat.setAddress(address);
            pat.setOpd(opd);
            pat.setMobileNo(mNo);
            pat.setMarriedStatus(jcGender.getSelectedItem().toString());
            pat.setPatientId(jcPatId.getSelectedItem().toString());
            boolean result = PatientsDao.updatePatient(pat);
               if(result){
                   JOptionPane.showMessageDialog(null, "Record updated successfully");
                   return;
               }
               else
               JOptionPane.showMessageDialog(null,"recod not updated!");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB");
        }
             
    }//GEN-LAST:event_btnUpdateActionPerformed

