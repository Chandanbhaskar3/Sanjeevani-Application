    private void loadPatientId() {
        try{
            List<String> patIds = PatientsDao.getAllPatientId();
            for(String patId : patIds){
                jcPatId.addItem(patId);
            }
            
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB");
            ex.printStackTrace();
        }
        
    }

    private void loadDoctorId() {
        try{
            List<String> docIds = DoctorsDao.getAllDoctorId();
            for(String docid : docIds){
               jcDocIds.addItem(docid);
            }
        }
        catch(SQLException es){
            JOptionPane.showMessageDialog(null, "Error in DB");
            es.printStackTrace();
        }
        
        
    }

    private boolean validateInputs() {
        
        fName = txtFirstName.getText().trim();
        lName = txtLastName.getText().trim();
        opd = txtOPD.getText().trim();
        mNo = txtPhone.getText().trim();
        address = txtAddress.getText().trim();
        city = txtCity.getText().trim();
//        age = txtAge.getText().trim();
        if(fName.isEmpty() || lName.isEmpty() || opd.isEmpty() || address.isEmpty() || city.isEmpty() || mNo.isEmpty()){
            return false;
        }
        if(mNo.length() == 10)
            return true;
        return true;
    }
